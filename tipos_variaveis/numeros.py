# int
numero_inteiro = 10
print("Inteiro = ", numero_inteiro)
# float
numero_decimal = 10.5   
print("Decimal = ", numero_decimal)
# type()
print("Tipo do inteiro = ", type(numero_inteiro))
# complex
numero_complexo = 1 + 2j
print("Complexo = ", numero_complexo)
# bool
numero_booleano = True
print("Booleano = ", numero_booleano)
# str
numero_string = "10.5"
print("String = ", numero_string)





# Soma +
soma = 1 + 1
print("1 + 1 = ", soma)
# Subtração -
subtracao = 10 - 5
print("10 - 5 = ", subtracao)
# Multiplicação *
multiplicacao = 2 * 3
print("2 * 3 = ", multiplicacao)
# Divisão /
divisao = 10 / 2
print("10 / 2 = ", divisao)
# Divisão inteira //
divisao_inteira = 10 // 3
print("10 // 3 = ", divisao_inteira)
# Módulo %
modulo = 10 % 3
print("10 % 3 = ", modulo)
# Exponenciação **
exponenciacao = 2 ** 3
print("2 ** 3 = ", exponenciacao)
# Raiz quadrada
import math 
raiz_quadrada = math.sqrt(16)
print("Raiz quadrada de 16 = ", raiz_quadrada)

